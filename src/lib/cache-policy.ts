/**
 * キャッシュ運用（Next.js App Router / Cloudflare Pages）
 *
 * - Data Cache は `fetch(url, { next: { revalidate } })` の秒数で制御する
 * - ここに「ページ種別ごとの更新頻度」を集約し、実装値を一貫させる
 *
 * 目標：
 * - 国別トップ/カテゴリ/最新系は「頻繁更新」だが、30秒更新は過剰になりやすい → 2分に寄せる
 * - 詳細（topic/column/quote）・朝刊（日付固定）は「基本固定」 → 1時間に寄せる
 */

export const CACHE_POLICY = {
  /**
   * 頻繁に更新される一覧系（国別トップ/カテゴリ/最新News/最新一覧/今日）
   * - 目安: 1〜5分 の中で、鮮度と負荷のバランスをとる
   */
  frequent: 120, // 2分

  /**
   * 生成後は基本固定のページ（朝刊 日付固定）や、更新頻度が低い詳細ページ
   * - 目安: 1〜24時間 の中で、まずは 1時間から開始
   */
  stable: 3600, // 1時間

  /**
   * sitemap/robots などのメタ系（頻繁に変わらないが、完全固定でもない）
   */
  meta: 3600, // 1時間

  /**
   * ルート（国選択）など、ほぼ固定のページ用
   */
  almostStatic: 86400, // 24時間
} as const


